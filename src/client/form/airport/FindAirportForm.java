    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (!tblAirports.getSelectionModel().isSelectionEmpty()) {

            int input = JOptionPane.showConfirmDialog(null,
                    "Do you want to proceed to delete this airport?", "Select an Option...", JOptionPane.YES_NO_OPTION);

            if (input == 0) {
                contDeleteAirport.SODeleteAirport();
                contFindSpecificAirport.prepareTable();
                contFindAllAirports.SOLoadListAirports();
                contFindAllAirports.fillTable();
            } else {
                return;
            }
        } else {
            lblStatus.setForeground(Color.WHITE);
            lblStatus.setBackground(new Color(219, 31, 31));
            lblStatus.setOpaque(true);
            lblStatus.setText("You didnt select any row!");
            lblStatus.setHorizontalAlignment(SwingConstants.CENTER);
            lblStatus.setVerticalAlignment(SwingConstants.CENTER);

        }    }//GEN-LAST:event_btnDeleteActionPerformed

