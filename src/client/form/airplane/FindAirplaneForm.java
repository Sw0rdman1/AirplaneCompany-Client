    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (!tblAvioni.getSelectionModel().isSelectionEmpty()) {

            int input = JOptionPane.showConfirmDialog(null,
                    "Do you want to proceed to delete this airplane?", "Select an Option...", JOptionPane.YES_NO_OPTION);

            if (input == 0) {
                contDeleteAirplane.SODeleteAirplane();
                contFindSpecificAirplane.prepareTable();
                contFindAllAirplanes.SOLoadListAirplanes();
                contFindAllAirplanes.fillTable();
            } else {
                return;
            }
        } else {
            lblStatus.setForeground(Color.WHITE);
            lblStatus.setBackground(new Color(219, 31, 31));
            lblStatus.setOpaque(true);
            lblStatus.setText("You didnt select any row!");
            lblStatus.setHorizontalAlignment(SwingConstants.CENTER);
            lblStatus.setVerticalAlignment(SwingConstants.CENTER);

        }    }//GEN-LAST:event_btnDeleteActionPerformed

